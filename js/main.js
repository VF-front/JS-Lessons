'use strict';

function calculateVolumeAndArea(num) {

    let str = '';
    let ob = num * num * num;
    let pl = 6 * num * num;

    if( num != null && num != '' && typeof num != 'string' && num > 0 && num % 1 == 0 ) {
        str = `Объем куба: ${ob}, площадь всей поверхности: ${pl}`
    } else {
        str = "При вычислении произошла ошибка";
    }

    return str

}
