function getCoupeNumber(num) {
    let str = Math.ceil(num / 4);

    if ( typeof num !== 'number' || num < 0 || num % 1 != 0 ) {
        str = "Ошибка. Проверьте правильность введенного номера места"
    } else if (num == 0 || num > 35) {
        str = "Таких мест в вагоне не существует";
    }

    return str;
}

